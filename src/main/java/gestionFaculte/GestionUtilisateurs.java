package gestionFaculte;

public class GestionUtilisateurs {
	 public void ajouterUtilisateur(String nom) {
	        System.out.println("Utilisateur ajouté : " + nom);
	    }

	    public void supprimerUtilisateur(String nom) {
	        System.out.println("Utilisateur supprimé : " + nom);
	    }
	    public void authentifierUtilisateur() {
	        System.out.println("Utilisateur authentifié avec succès.");
	    }

	    public void deconnecterUtilisateur() {
	        System.out.println("Utilisateur déconnecté.");
	    }
	

}
